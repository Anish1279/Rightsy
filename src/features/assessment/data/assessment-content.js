export function getQuizModuleById(moduleId) {
  return quizModules.find((module) => module.id === moduleId) || null;
}

export function getQuizModuleByLevel(level) {
  const parsedLevel = Number.parseInt(String(level), 10);
  return quizModules.find((module) => module.numericLevel === parsedLevel) || null;
}

export function getSrtMissionById(missionId) {
  return srtMissions.find((mission) => mission.id === missionId) || null;
}

export function getNextQuizModule(moduleId) {
  const index = quizModules.findIndex((module) => module.id === moduleId);
  return index >= 0 ? quizModules[index + 1] || null : null;
}

export function getNextSrtMission(missionId) {
  const index = srtMissions.findIndex((mission) => mission.id === missionId);
  return index >= 0 ? srtMissions[index + 1] || null : null;
}

export function getAssessmentByKind(kind, moduleId) {
  if (kind === "QUIZ") return getQuizModuleById(moduleId);
  if (kind === "SRT") return getSrtMissionById(moduleId);
  return null;
}
